function cm=no_green(N)
% The GMT no_green colormap
%
% cm = no_green(N)
%
% N	= The desired length of the colormap.
% cm	= The resulting RGB colormap 
% 
% See also CMOCEAN M_COLMAP GRAPH3D COLORCET LINSPACE COLORMAP ECOLORMAP ECOLORBAR

no_green = [ 32  96 255; ...
	     32 159 255; ...
	     32 191 255; ...
	      0 207 255; ...
	     42 255 255; ...
	     85 255 255; ...
	    127 255 255; ...
	    170 255 255; ...
	    255 255  84; ...
	    255 240   0; ...
	    255 191   0; ...
	    255 168   0; ...
	    255 138   0; ...
	    255 112   0; ...
	    255  77   0; ...
	    255   0   0]/255;

cm1=no_green(1:8,:); % Split it where the green part is
cm2=no_green(9:16,:);
m=8;
cx1=linspace(1,m,floor(N/2));	% resampling points,
cx2=linspace(1,m,ceil(N/2));	% but not in the green part
% linear interpolation on each color-column:
for i=1:3, 
  cmx1(:,i)=interp1([1:m]',cm1(:,i),cx1);	
  cmx2(:,i)=interp1([1:m]',cm2(:,i),cx2);
end
cm=[cmx1;cmx2]; 
